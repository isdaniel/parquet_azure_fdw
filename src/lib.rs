    #[cfg(feature = "pg_test")]
    #[pg_test]
    fn pg_update_then_select() {
        // Same session: UPDATE then SELECT. The FDW must NOT cache stale
        // blob bytes across statements — the second statement issues a
        // fresh GET against the fake and sees the new etag/body.
        let container = "pg-upd-then-sel";
        let _fake = setup_two_row_modify("upd_then_sel", container);

        Spi::run("UPDATE mod_upd_then_sel SET name = 'fresh' WHERE id = 1").unwrap();
        let v: Option<String> =
            Spi::get_one("SELECT name FROM mod_upd_then_sel WHERE id = 1").unwrap();
        assert_eq!(v.as_deref(), Some("fresh"));

        // And a follow-up UPDATE on the other row works on top of the
        // first one's committed state.
        Spi::run("UPDATE mod_upd_then_sel SET name = 'also' WHERE id = 2").unwrap();
        let v2: Option<String> =
            Spi::get_one("SELECT name FROM mod_upd_then_sel WHERE id = 2").unwrap();
        assert_eq!(v2.as_deref(), Some("also"));
        // The earlier edit must persist.
        let v1: Option<String> =
            Spi::get_one("SELECT name FROM mod_upd_then_sel WHERE id = 1").unwrap();
        assert_eq!(v1.as_deref(), Some("fresh"));
    }
