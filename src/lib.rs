    // ---------------------------------------------------------------------
    // End-to-end INSERT / COPY suite (Task 17).
    //
    // The write path uploads a single block blob per `EndForeignModify` call,
    // named `{prefix}{ts}-{uuid}.parquet` when the table's `filename` option
    // contains a `*` (treated as a prefix). For round-trip checks, each test
    // creates TWO foreign tables on the same container:
    //   - a write-side table whose `filename` is a prefix like `'inserts/'` or
    //     a glob like `'inserts/*'` so each INSERT lands in a fresh blob,
    //   - a read-side table whose `filename` is the glob `'inserts/*.parquet'`
    //     so the just-written blob is picked up by the scan.
    //
    // ROLLBACK semantics (spec §7.2): Postgres DOES call
    // `EndForeignModify` on the abort path, so the FDW must check
    // `IsAbortedTransactionBlockState()` and skip the upload when aborted.
    // The boxed `ModifyState` is then dropped (freeing builders/writer) and
    // no blob is created.
    #[cfg(feature = "pg_test")]
    fn install_insert_objects_sql(
        suffix: &str,
        fake: &FakeBlobStore,
        container: &str,
        write_filename: &str,
        read_filename: &str,
    ) -> String {
        let sas = fake.sas_url(container);
        format!(
            r#"
            DROP FOREIGN TABLE IF EXISTS events_w_{suffix} CASCADE;
            DROP FOREIGN TABLE IF EXISTS events_r_{suffix} CASCADE;
            DROP USER MAPPING IF EXISTS FOR CURRENT_USER SERVER az_{suffix};
            DROP SERVER IF EXISTS az_{suffix} CASCADE;

            CREATE SERVER az_{suffix} FOREIGN DATA WRAPPER parquet_azure_fdw
              OPTIONS (account_name 'fakeaccount',
                       endpoint 'blob.core.windows.net',
                       auth_method 'sas_url');
            CREATE USER MAPPING FOR CURRENT_USER SERVER az_{suffix}
              OPTIONS (sas_url '{sas}');
            CREATE FOREIGN TABLE events_w_{suffix} (id bigint, name text)
              SERVER az_{suffix}
              OPTIONS (container '{container}', filename '{write_filename}');
            CREATE FOREIGN TABLE events_r_{suffix} (id bigint, name text)
              SERVER az_{suffix}
              OPTIONS (container '{container}', filename '{read_filename}');
            "#
        )
    }

