    /// Boilerplate SQL that wires Server, UserMapping, and ForeignTable for
    /// `container` against a single blob (or glob pattern) `filename`. The
    /// `endpoint` SERVER option is a placeholder for the SasUrl auth path —
    /// the full pre-signed URL on the user mapping carries everything the
    /// SDK needs to reach the fake.
    #[cfg(feature = "pg_test")]
    fn install_objects_sql(
        suffix: &str,
        fake: &FakeBlobStore,
        container: &str,
        filename: &str,
    ) -> String {
        let sas = fake.sas_url(container);
        format!(
            r#"
            DROP FOREIGN TABLE IF EXISTS events_{suffix} CASCADE;
            DROP USER MAPPING IF EXISTS FOR CURRENT_USER SERVER az_{suffix};
            DROP SERVER IF EXISTS az_{suffix} CASCADE;

            CREATE SERVER az_{suffix} FOREIGN DATA WRAPPER parquet_azure_fdw
              OPTIONS (account_name 'fakeaccount',
                       endpoint 'blob.core.windows.net',
                       auth_method 'sas_url');
            CREATE USER MAPPING FOR CURRENT_USER SERVER az_{suffix}
              OPTIONS (sas_url '{sas}');
            CREATE FOREIGN TABLE events_{suffix} (id bigint, name text)
              SERVER az_{suffix}
              OPTIONS (container '{container}', filename '{filename}');
            "#
        )
    }

