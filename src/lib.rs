    // End-to-end UPDATE / DELETE suite (Task 15).
    //
    // Each test:
    //   1. Seeds a container with a `data.parquet` of `(id bigint, name text)`
    //      via the harness `put_blob` + `build_simple_parquet` path.
    //   2. Wires SERVER / USER MAPPING / FOREIGN TABLE against that single
    //      blob (no glob — UPDATE/DELETE addresses fixed blob names).
    //   3. Drives the FDW via SPI and asserts via SELECT or, where the
    //      assertion is "no Azure I/O happened", via `read_etag` against
    //      the underlying blob.
    //
    // Schema-mapping note: `build_simple_parquet` produces parquet columns
    // named `id` (Int64) and `name` (Utf8). The foreign table's column
    // names are matched by name in `pg_attrs_to_arrow_schema`, so the
    // foreign table column names must match the parquet column names.

    #[cfg(feature = "pg_test")]
    fn install_modify_objects_sql(suffix: &str, fake: &FakeBlobStore, container: &str) -> String {
        let sas = fake.sas_url(container);
        format!(
            r#"
            DROP FOREIGN TABLE IF EXISTS mod_{suffix} CASCADE;
            DROP USER MAPPING IF EXISTS FOR CURRENT_USER SERVER azm_{suffix};
            DROP SERVER IF EXISTS azm_{suffix} CASCADE;

            CREATE SERVER azm_{suffix} FOREIGN DATA WRAPPER parquet_azure_fdw
              OPTIONS (account_name 'fakeaccount',
                       endpoint 'blob.core.windows.net',
                       auth_method 'sas_url');
            CREATE USER MAPPING FOR CURRENT_USER SERVER azm_{suffix}
              OPTIONS (sas_url '{sas}');
            CREATE FOREIGN TABLE mod_{suffix} (id bigint, name text)
              SERVER azm_{suffix}
              OPTIONS (container '{container}', filename 'data.parquet');
            "#
        )
    }

    /// Seed a fresh fake container with a known two-row `data.parquet` and
    /// install the foreign-table SQL stack. Returns the fake so each test
    /// can use it for direct etag/blob inspection.
