    // ---------------------------------------------------------------------
    // Live Azure smoke test (Task 22).
    //
    // OPT-IN: This `#[pg_test]` runs against a REAL Azure storage container
    // when the `AZURE_TEST_SAS_URL` environment variable is set to a full
    // container SAS URL of the form
    //   `https://<account>.blob.core.windows.net/<container>?<sas-querystring>`
    // with at least `racwl` permissions (read, add, create, write, list).
    //
    // Without the env var the test is a no-op early-return so CI without
    // the secret is a pass. The SAS URL is parsed to extract the account
    // and container; the `endpoint` SERVER option is a no-op placeholder
    // for the SasUrl auth path (the full pre-signed URL carries everything
    // we need) but is still required by the SERVER options validator.
    //
    // CLEANUP: each invocation uses a unique `smoke-<uuid>/` blob prefix so
    // concurrent runs don't collide. We do NOT delete blobs at end of test
    // (the FDW has no DELETE path); the prefix is printed so a human can
    // clean up via `az storage blob delete-batch` if desired.
    fn parse_sas_url(url: &str) -> Option<(String, String, String)> {
        let u = url::Url::parse(url).ok()?;
        let host = u.host_str()?;
        let account = host.split('.').next()?.to_string();
        let path = u.path().trim_start_matches('/');
        let container = path.split('/').next()?.to_string();
        if account.is_empty() || container.is_empty() {
            return None;
        }
        Some((account, container, url.to_string()))
    }

    #[pg_test]
    fn live_smoke_write_then_read() {
        let Some((account, container, sas)) = std::env::var("AZURE_TEST_SAS_URL")
            .ok()
            .and_then(|s| parse_sas_url(&s))
        else {
            eprintln!("AZURE_TEST_SAS_URL not set; skipping live Azure smoke test");
            return;
        };

        let prefix = format!("smoke-{}", uuid::Uuid::new_v4());
        eprintln!(
            "live_smoke: account={account} container={container} prefix={prefix} \
             (blobs will be left in place; clean up manually if needed)"
        );

        let write_filename = format!("{prefix}/*");
        let read_filename = format!("{prefix}/*.parquet");

        Spi::run(&format!(
            r#"
            DROP FOREIGN TABLE IF EXISTS live_w CASCADE;
            DROP FOREIGN TABLE IF EXISTS live_r CASCADE;
            DROP USER MAPPING IF EXISTS FOR CURRENT_USER SERVER live_az;
            DROP SERVER IF EXISTS live_az CASCADE;

            CREATE SERVER live_az FOREIGN DATA WRAPPER parquet_azure_fdw
              OPTIONS (account_name '{account}',
                       endpoint 'blob.core.windows.net',
                       auth_method 'sas_url');
            CREATE USER MAPPING FOR CURRENT_USER SERVER live_az
              OPTIONS (sas_url '{sas}');
            CREATE FOREIGN TABLE live_w (id bigint, name text)
              SERVER live_az
              OPTIONS (container '{container}', filename '{write_filename}');
            CREATE FOREIGN TABLE live_r (id bigint, name text)
              SERVER live_az
              OPTIONS (container '{container}', filename '{read_filename}');
            INSERT INTO live_w VALUES (1,'alpha'),(2,'beta'),(3,'gamma');
            "#
        ))
        .expect("install + insert against live Azure");

        let count = Spi::get_one::<i64>("SELECT COUNT(*) FROM live_r WHERE id IN (1,2,3)")
            .expect("count query")
            .expect("count not null");
        assert_eq!(count, 3);
    }

