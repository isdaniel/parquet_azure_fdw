    #[cfg(feature = "pg_test")]
    #[pg_test]
    fn rollback_before_end_modify_drops_buffer() {
        // Per spec §7.2: a transaction abort BEFORE `EndForeignModify`
        // commits (i.e. before the upload happens) must not produce a blob.
        // `end_foreign_modify` is in fact called on the abort path too, so
        // the implementation must check `IsAbortedTransactionBlockState()`
        // and skip the upload. We verify by enumerating the container's
        // blob list directly — `SELECT COUNT(*)` over a foreign table can't
        // distinguish "no blob present" from "an empty-rows parquet was
        // uploaded" (both yield 0 rows).
        let fake = FakeBlobStore::start_blocking();
        let container = "test-rollback-noop";

        // Pre-condition: container is empty.
        let before = fake.list_blobs(container, None);
        assert!(
            before.is_empty(),
            "container should start empty; found {before:?}"
        );

        Spi::run(&install_insert_objects_sql(
            "rb",
            &fake,
            container,
            "aborted/*",
            "aborted/*.parquet",
        ))
        .expect("install foreign objects");

        // Spi::connect inside pgrx wraps each `Spi::run` in its own
        // subtransaction, so BEGIN/INSERT/ROLLBACK as separate top-level
        // commands don't share state. Drive the whole sequence as a single
        // SQL string so it executes in one SPI connection.
        let r = Spi::run(
            "BEGIN; \
             INSERT INTO events_w_rb VALUES (99, 'never'); \
             ROLLBACK;",
        );
        // The ROLLBACK may surface as an Spi error in some pgrx versions; we
        // tolerate it. The invariant we care about is "no blob was uploaded",
        // checked next.
        let _ = r;

        // Source of truth: ask the fake directly. The container must contain
        // zero blobs — not even an empty-rows parquet (header+footer only).
        let after = fake.list_blobs(container, None);
        assert!(
            after.is_empty(),
            "rollback before EndForeignModify must not upload any blob; \
             found: {after:?}"
        );
    }

