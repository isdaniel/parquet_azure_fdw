/// PG backend init hook. Runs once per backend (after `pg_module_magic!`
/// registers the library). We use it to register our `XactCallback` for
/// staging-blob cleanup on transaction abort — see
/// `fdw::modify::coordinator::install_xact_callback_once`.
#[pg_guard]
extern "C-unwind" fn _PG_init() {
    crate::fdw::modify::coordinator::install_xact_callback_once();
}

/// FDW handler — returns an `FdwRoutine` populated with our callbacks.
/// pgrx maps the `PgBox<FdwRoutine>` return into PG's `fdw_handler` type, so
/// no custom `sql=` override is needed: the generated `CREATE FUNCTION` will
/// already say `RETURNS fdw_handler`.
#[pg_extern]
fn parquet_azure_fdw_handler() -> PgBox<pg_sys::FdwRoutine, pgrx::AllocatedByRust> {
    fdw::build_routine()
}

/// FDW validator — invoked by PG for every `CREATE/ALTER SERVER`,
/// `USER MAPPING`, or `FOREIGN TABLE` against this wrapper. We hand the raw
/// `text[]` straight to [`fdw::options::validate`] which dispatches by
/// catalog OID to the matching parser; any [`error::FdwError`] is reraised
/// as a PG `ERROR`.
#[pg_extern]
fn parquet_azure_fdw_validator(options: Vec<Option<String>>, catalog: pg_sys::Oid) {
    if let Err(e) = fdw::options::validate(options, catalog) {
        crate::error::raise(e);
    }
}

// Create the FDW itself. The handler/validator CREATE FUNCTION lines are
// auto-generated by pgrx from the `#[pg_extern]` attributes above, so we
// only need to wire them into a FOREIGN DATA WRAPPER.
extension_sql!(
    r#"
    CREATE FOREIGN DATA WRAPPER parquet_azure_fdw
        HANDLER parquet_azure_fdw_handler
        VALIDATOR parquet_azure_fdw_validator;
    "#,
    name = "parquet_azure_fdw_bootstrap",
    requires = [parquet_azure_fdw_handler, parquet_azure_fdw_validator],
);

