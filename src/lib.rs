    fn pg_delete_where_false_is_noop() {
        // DELETE matching zero rows must produce ZERO Azure I/O — the
        // accumulator stays empty so `commit_plan` never enters its
        // per-blob GET/PUT loop, and the blob's etag is unchanged.
        let container = "pg-del-false";
        let fake = setup_two_row_modify("del_false", container);

        let pre_etag = fake
            .read_etag(container, "data.parquet")
            .expect("blob exists pre");

        Spi::run("DELETE FROM mod_del_false WHERE id = 99").unwrap();

        let post_etag = fake
            .read_etag(container, "data.parquet")
            .expect("blob exists post");
        assert_eq!(
            pre_etag, post_etag,
            "no rows matched — no Azure I/O expected"
        );

        // Row count unchanged.
        let n: Option<i64> = Spi::get_one("SELECT count(*)::bigint FROM mod_del_false").unwrap();
        assert_eq!(n, Some(2));
    }

    #[cfg(feature = "pg_test")]
    #[pg_test]
    fn pg_rollback_discards_pending_edits() {
        // BEGIN; UPDATE; ROLLBACK; — `end_foreign_modify` sees
        // `IsAbortedTransactionBlockState()` and must skip `commit_plan`,
        // leaving the blob unchanged.
        //
        // As in the INSERT-side rollback test (`rollback_before_end_modify_drops_buffer`),
        // BEGIN/UPDATE/ROLLBACK must be executed as a single SPI batch — issuing
        // them as separate `Spi::run` calls wraps each in its own subtransaction
        // and the BEGIN/ROLLBACK pair becomes a no-op.
        let container = "pg-rb";
        let fake = setup_two_row_modify("rb", container);

        let pre_etag = fake
            .read_etag(container, "data.parquet")
            .expect("blob exists pre");

        let r = Spi::run(
            "BEGIN; \
             UPDATE mod_rb SET name = 'z' WHERE id = 1; \
             ROLLBACK;",
        );
        let _ = r; // ROLLBACK may surface as an Spi error in some pgrx versions.

        let post_etag = fake
            .read_etag(container, "data.parquet")
            .expect("blob exists post");
        assert_eq!(
            pre_etag, post_etag,
            "ROLLBACK must not commit edits to the blob"
        );

        // And a fresh SELECT sees the original row, not the rolled-back edit.
        let v: Option<String> = Spi::get_one("SELECT name FROM mod_rb WHERE id = 1").unwrap();
        assert_eq!(v.as_deref(), Some("a"));
    }

